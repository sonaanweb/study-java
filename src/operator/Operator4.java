package operator;

public class Operator4 {

    public static void main(String[] args) {
        // 가독성을 위하여 괄호를 추가해주는 과정
        int sum3 = 2 * 2 + 3 * 3; // ( 2 * 2 ) + ( 3 * 3 )
        int sum4 = (2 * 2) + (3 * 3); // 위와 같다
        System.out.println(sum3);
        System.out.println(sum4);
    }
}
